package Tarifa;

public class Tarifa {

	double tarifaBase;
	int tipoTarifa;
	
	

	public double getTarifaBase() {
		return tarifaBase;
	}
	public void setTarifaBase(double tarifaBase) {
		this.tarifaBase = tarifaBase;
	}
	public int getTipoTarifa() {
		return tipoTarifa;
	}
	public void setTipoTarifa(int tipoTarifa) {
		this.tipoTarifa = tipoTarifa;
	}
	
	
	
	
}
